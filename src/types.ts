// ── Типы данных приложения «Пульт» ──────────────────────────────
// Здесь описано, из чего состоит один пункт (Item) и одна запись
// в журнале операций (Op). Это «словарь» всего приложения.

/** Вид пункта: «моё» дело или «жду от кого-то». */
export type ItemKind = 'mine' | 'waiting'

/** Состояние пункта. */
export type ItemStatus =
  | 'open' // активен, виден в Сводке
  | 'done' // закрыт (выполнен)
  | 'trashed' // в корзине (мягкое удаление)

/** Один пункт — задача, ожидание. */
export interface Item {
  id: string
  kind: ItemKind
  title: string
  /** От кого ждём (только для kind === 'waiting'). */
  who?: string
  /** Срок в миллисекундах (Date.now). Пусто — срока нет. */
  due?: number
  status: ItemStatus
  createdAt: number
  updatedAt: number
  /** Когда пункт был закрыт (для «периода благодати»). */
  doneAt?: number
  /** Когда пункт был отправлен в корзину. */
  trashedAt?: number
}

// ── Журнал операций (op-log) ────────────────────────────────────
// Каждое изменение записывается отдельной операцией. По ней можно
// понять, что именно поменялось, и откатить назад (Undo).

export type OpType =
  | 'create' // создан пункт
  | 'close' // пункт закрыт
  | 'reopen' // пункт снова открыт
  | 'trash' // пункт в корзину
  | 'restore' // пункт восстановлен из корзины
  | 'edit' // отредактирован

/** Одна запись в журнале. before/after — снимки пункта до и после. */
export interface Op {
  id: string
  type: OpType
  itemId: string
  at: number
  /** Снимок пункта ДО операции (пусто, если пункт только создан). */
  before?: Item
  /** Снимок пункта ПОСЛЕ операции. */
  after?: Item
  /** Уже отменена? Чтобы не откатывать дважды. */
  undone?: boolean
}
