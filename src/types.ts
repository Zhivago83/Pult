/** Вид пункта: «моё» дело или «жду от кого-то». */
export type Kind = 'mine' | 'waiting'

/** Роль человека: «команда» (кому делегирую) или «исполнитель» (внешний). */
export type Role = 'team' | 'contractor'

/**
 * Человек, от которого чего-то ждём. Людей узнаём по имени (поле `who`
 * у пунктов). Здесь храним только то, что нельзя вычислить из пунктов, —
 * роль. Надёжность считается из истории на лету.
 */
export interface Person {
  /** Имя — совпадает с полем `who` у пунктов. Ключ человека. */
  name: string
  role: Role
}

/**
 * Состояние пункта.
 *  open    — активен, виден в Сводке
 *  done    — закрыт (выполнен)
 *  trashed — в Корзине (мягкое удаление)
 */
export type Status = 'open' | 'done' | 'trashed'

/** Пункт — единица работы: задача, ожидание, проект. */
export interface Item {
  id: string
  kind: Kind
  /** Короткая формулировка: что нужно сделать / чего ждём. */
  title: string
  /** Для «жду от кого-то»: от кого ждём (владелец). */
  who?: string
  /** Проект, к которому относится пункт. Может отсутствовать. */
  project?: string
  /** Срок (метка времени, мс). Может отсутствовать. */
  dueAt?: number
  status: Status
  /** Когда закрыт. */
  closedAt?: number
  /** Когда отправлен в Корзину. */
  trashedAt?: number
  /**
   * «Период благодати»: до этого момента закрытый пункт ещё
   * показывается зачёркнутым в Сводке, потом исчезает.
   */
  graceUntil?: number
  createdAt: number
  updatedAt: number
}

/** Типы операций для журнала (op-log) — понятны человеку. */
export type OpType =
  | 'create' // создание пункта
  | 'close' // закрытие (тап по кружку)
  | 'reopen' // повторное открытие
  | 'trash' // мягкое удаление в Корзину
  | 'restore' // восстановление из Корзины
  | 'edit' // редактирование полей
  | 'comment' // комментарий в истории пункта
  | 'remind' // отметка «напомнил» (для «жду от кого-то»)
  | 'setRole' // смена роли человека (команда/исполнитель)

/**
 * Op — запись журнала операций.
 * Хранит снимок ДО и ПОСЛЕ изменения, поэтому любую операцию можно
 * отменить (Undo): просто вернуть состояние `before`.
 * Большинство операций — про пункт (before/after: Item). Операция
 * `setRole` — про человека (personBefore/personAfter).
 */
export interface Op {
  id: string
  ts: number
  type: OpType
  /** Ключ затронутого объекта: id пункта либо имя человека. */
  itemId: string
  /** Снимок пункта до операции. null — пункта ещё не существовало. */
  before: Item | null
  /** Снимок пункта после операции. null — пункт полностью удалён. */
  after: Item | null
  /** Текст — для операций-комментариев (type === 'comment'). */
  text?: string
  /** Снимок человека до операции (для type === 'setRole'). */
  personBefore?: Person | null
  /** Снимок человека после операции (для type === 'setRole'). */
  personAfter?: Person | null
  /** Была ли операция уже отменена. */
  undone?: boolean
}
